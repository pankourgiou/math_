import Foundation

// Addition
let sum = -1300 + 1300
print("Sum: \(sum)") 

// Subtraction
let difference = 10000 - 10000
print("Difference: \(difference)") 

// Multiplication
let product = 1000000 * 0
print("Product: \(product)") 

// Division
let quotient = 100000 / 100000
print("Quotient: \(quotient)") 

// Modulus (Remainder)
let remainder = 3 % 3
print("Remainder: \(remainder)") 

// Exponentiation (using pow function from Foundation)
let base = 2.0
let exponent = 3.0
let power = pow(base, exponent)
print("Power: \(power)") 
